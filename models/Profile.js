const mongoose = require('mongoose');

// Model for profile
const ProfileSchema = new mongoose.Schema({
  // Reference to user-model
  user: {
    // type: objectId -> Connected with user id
    type: mongoose.Schema.Types.ObjectId,
    // referece to User model
    ref: 'user'
  },
  // Company working in
  company: {
    type: String
  },
  website: {
    type: String
  },
  location: {
    type: String
  },
  // Work Status -> Example: Senior Developer\Student
  status: {
    type: String,
    required: true
  },
  skills: {
    type: [String],
    required: true
  },
  bio: {
    type: String
  },
  githubusername: {
    type: String
  },
  // Work experience
  experience: [
    {
      title: {
        type: String,
        required: true
      },
      location: {
        type: String
      },
      from: {
        type: Date,
        required: true
      },
      to: {
        type: Date
      },
      current: {
        type: Boolean,
        default: false
      },
      description: {
        type: String
      }
    }
  ],
  // Education data
  education: [
    {
      school: {
        type: String,
        required: true
      },
      degree: {
        type: String,
        required: true
      },
      fieldofstudy: {
        type: String,
        required: true
      },
      from: {
        type: Date,
        required: true
      },
      to: {
        type: Date
      },
      current: {
        type: Boolean,
        default: false
      },
      description: {
        type: String
      }
    }
  ],
  // Social media links
  social: {
    youtube: {
      type: String
    },
    twitter: {
      type: String
    },
    facebook: {
      type: String
    },
    linkedin: {
      type: String
    },
    instagram: {
      type: String
    }
  },
  date: {
    type: Date,
    default: Date.now
  }
});

module.exports = Profile = mongoose.model('profile', ProfileSchema);
